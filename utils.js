const yearlyBudget = data.reduce((acc, item) => {
  if (!acc[item.year]) acc[item.year] = { totalBudget: 0, count: 0 };
  acc[item.year].totalBudget += item.budget;
  acc[item.year].count += 1;
  return acc;
}, {});

